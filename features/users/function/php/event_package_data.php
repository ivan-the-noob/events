<script>
   function updateDynamicCosts() {
    const activeOptionsContainer = document.querySelector('.form-group[id^="Other-"]:not([style*="display: none"])');

    if (activeOptionsContainer) {
        const selectedPax = activeOptionsContainer.querySelector('input[type="radio"]:checked');

        if (selectedPax) {
            const paxValue = selectedPax.value;

            const glazingTableCheckbox = activeOptionsContainer.querySelector('input[type="checkbox"][id*="glazing"]');
            let glazingTableCost = 0;
            if (paxValue === '50 Pax') glazingTableCost = 6000;
            else if (paxValue === '60 Pax') glazingTableCost = 7000;
            else if (paxValue === '80 Pax') glazingTableCost = 10000;
            else if (paxValue === '100 Pax') glazingTableCost = 13000;
            else if (paxValue === '150 Pax') glazingTableCost = 17000;
            else if (paxValue === '200 Pax') glazingTableCost = 25000;
            if (glazingTableCheckbox) {
                glazingTableCheckbox.setAttribute('data-cost', glazingTableCost);
            }

            const cateringCheckbox = activeOptionsContainer.querySelector('input[type="checkbox"][id*="catering"]');
            let cateringCost = 0;
            if (paxValue === '50 Pax') cateringCost = 13000;
            else if (paxValue === '60 Pax') cateringCost = 16000;
            else if (paxValue === '80 Pax') cateringCost = 19000;
            else if (paxValue === '100 Pax') cateringCost = 22000;
            else if (paxValue === '150 Pax') cateringCost = 26000;
            else if (paxValue === '200 Pax') cateringCost = 35000;
            if (cateringCheckbox) {
                cateringCheckbox.setAttribute('data-cost', cateringCost);
            }
        }

        calculateTotalCost(activeOptionsContainer);
    }
}

function calculateTotalCost(activeOptionsContainer) {
    let totalCost = 0;

    const selectedOptions = activeOptionsContainer.querySelectorAll('input[name="event_options[]"]:checked');
    selectedOptions.forEach(option => {
        totalCost += parseFloat(option.getAttribute('data-cost') || 0);
    });

    document.getElementById('event-cost').value = totalCost;
}

document.addEventListener('DOMContentLoaded', function () {
    document.querySelectorAll('.form-group[id^="Other-"]').forEach(container => {
        container.addEventListener('change', () => {
            updateDynamicCosts();
        });
    });

    updateDynamicCosts();
});


</script>
